#include <stdio.h>
#include<time.h>
void matrix_multiply() {
	const int n = 10;
	int A[n][n] = { {15,7,3,6,7,9,1,8,5,4},{2,5,3,6,6,8,1,2,3,5},{2,5,3,6,6,8,1,2,3,5},{3,11,8,5,9,9,8,4,9,5},{5,1,1,6,5,7,6,3,2,1},{3,11,8,5,9,9,8,4,9,5},{2,5,3,6,6,8,1,2,3,5},{5,1,1,6,5,7,6,3,2,1},{2,5,3,6,6,8,1,2,3,5},{5,1,1,6,5,7,6,3,2,1} };
	int B[n][n] = { {3,11,8,5,9,9,8,4,9,5},{5,1,1,6,5,7,6,3,2,1},{3,11,8,5,9,9,8,4,9,5},{2,5,3,6,6,8,1,2,3,5},{2,5,3,6,6,8,1,2,3,5},{2,5,3,6,6,8,1,2,3,5},{5,1,1,6,5,7,6,3,2,1},{2,5,3,6,6,8,1,2,3,5},{5,1,1,6,5,7,6,3,2,1},{2,5,3,6,6,8,1,2,3,5} };
	int S[n][n];

	int i, j, k;
	int sum;

	for (i = 0; i < n; i++) {
		for (j = 0; j < n; j++) {
			sum = 0;
			for (k = 0; k < n; k++) {
				sum += A[i][k] * B[k][j];
			}
			S[i][j] = sum;
		}
	}

	for (i = 0; i < n; i++) {
		for (j = 0; j < n; j++) {
			printf("%d\t", S[i][j]);
		}
		printf("\n");
	}

}

int main(void) {
	clock_t start, end;
	float res;
	start = clock();
	matrix_multiply();
	end = clock();
	res = (float)(end - start);
	printf("½Ã°£=%f ms.", res);
	return 0;
}
